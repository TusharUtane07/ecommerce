"use client";
import type { Metadata } from "next";
import localFont from "next/font/local";
import "./globals.css";
import { Toaster } from "react-hot-toast";
import Footer from "@/sections/Footer";
import Header from "@/sections/Header";
import { usePathname } from "next/navigation";
import { persistor, store } from "@/redux/store";
import { Provider } from "react-redux";
import { PersistGate } from "redux-persist/integration/react";
import GotoTopButton from "@/components/GotoTopButton";

const geistSans = localFont({
	src: "./fonts/GeistVF.woff",
	variable: "--font-geist-sans",
	weight: "100 900",
});
const geistMono = localFont({
	src: "./fonts/GeistMonoVF.woff",
	variable: "--font-geist-mono",
	weight: "100 900",
});

const metadata: Metadata = {
	title: "Eshop | Shopping Platform",
	description: "Generated by create next app",
};

export default function RootLayout({
	children,
}: Readonly<{
	children: React.ReactNode;
}>) {
	const pathname = usePathname();

	const noHeaderPaths = ["/sign-in", "/sign-up"];

	return (
		<html lang="en" data-theme="light">
			<head>
				<link rel="shortcut icon" href="/favicon.png" type="image/x-icon" />
				<title>Nexa | Sneakers Platform</title>
			</head>
			<body
				className={`${geistSans.variable} ${geistMono.variable} antialiased px-2`}>
				<Provider store={store}>
					<PersistGate loading={null} persistor={persistor}>
						{!noHeaderPaths.includes(pathname) && <Header />}
						{children}
						<Footer />
						<GotoTopButton />
						<Toaster></Toaster>
					</PersistGate>
				</Provider>
			</body>
		</html>
	);
}
